def makeGraphicsDirectory(dataset_name, graphics_folder):
  graphics_folder_name = getTestScenarioDatasetFullPath(dataset_name) + graphics_folder

 # user and user group read, write and execute permission. Others with read and execute(transverse) permission
  permissions =  0o775

  # permissive makedir. Doesnt throw an exception if folder exists
  try:
      os.mkdir(graphics_folder_name, permissions)
  except Exception as e:
    if e.errno == errno.EEXIST: # if folder exist, ignore exception
        pass
    else:
        raise

  return graphics_folder_name
